        static bool[] visited; 

        public static void BFS(int start)

        {


            visited = Enumerable.Repeat<bool>(false, adjMatrix.GetLength(0)).ToArray();

            Queue<int> q = new Queue<int>();

            q.Enqueue(start);


            visited[0] = true;

            int vis;

            while (q.Count != 0)
            {

                vis = q.Dequeue();

                Console.Write($"{vis} -> ");

                for (int i = 0; i < adjMatrix.GetLength(0); i++)
                {

                    if (adjMatrix[vis, i] == 1 && (!visited[i]))
                    {

                        q.Enqueue(i);

                        visited[i] = true;

                    }

                }

            }

        }

        public static void DFS(int start)
        {
            visited = Enumerable.Repeat<bool>(false, adjMatrix.GetLength(0)).ToArray();
            DfsMethod(start);
        }
        static void DfsMethod(int start)

